var updateModel = function(model,caption,value) {
   if (model.hasOwnProperty(caption)) {
      model[caption] = value;
   }

   model.atributos.forEach(function(atributo){
      if (atributo.caption == caption) {
         atributo.value = value;
      }
   })
}

var data = {
  "tipoEquipos": generarTipoEquipos(),
  "edificios":[]
};

var rows = [];

for(var index in model) {
  var row = model[index];

  /* edificio */
  var edificio = data.edificios.find(function(edificio){
    return edificio.nombre == row['EDIFICIO'].trim()
  });

  if (!edificio) {
    edificio = edificioModel(data.edificios.length + 1, row['EDIFICIO'].trim());
    data.edificios.push(edificio);
  }

  /* piso */
  var piso = edificio.pisos.find(function(piso){
    return piso.nombre == row['PISO'].trim()
  });

  if (!piso) {
    piso = pisoModel(edificio.pisos.length + 1, row['PISO'].trim());
    edificio.pisos.push(piso);
  }

  /* sala */
  var sala = piso.salas.find(function(sala){
    return sala.nombre == row['SALA'].trim()
  });

  if (!sala) {
    sala = salaModel(piso.salas.length + 1, row['SALA'].trim());
    piso.salas.unshift(sala);
  }

  /* fila */
  var fila = sala.filas.find(function(fila){
    return fila.nombre == row['FILA'].trim()
  });

  if (!fila) {
    fila = filaModel(sala.filas.length + 1, row['FILA'].trim());
    sala.filas.unshift(fila);
  }

  /* rack */
  var rack = fila.racks.find(function(rack){
    return rack.nombre == row['RACK'].trim()
  });

  if (!rack) {
    rack = rackModel(fila.racks.length + 1, row['RACK'].trim());
    fila.racks.unshift(rack);
  }

  if (fila.racks.length > sala.posiciones) {
    sala.posiciones = fila.racks.length;
    if (sala.posiciones < 10) {
      sala.posiciones = 10;
    }
  }

  /* equipo */
  var equipo = rack.equipos.find(function(equipo){
    return equipo.nombre == row['EQUIPO'].trim()
  });

  if (!equipo) {
    equipo = equipoModel(rack.equipos.length + 1, row['EQUIPO'].trim());
    rack.equipos.unshift(equipo);
  }

  rows.push({
    edificio:edificio,
    piso:piso,
    sala:sala,
    fila:fila,
    rack:rack,
    equipo:equipo
  });

}


/* Totales, posiciones y topologias */

data.edificios.forEach(function(edificio){

  updateModel(edificio, 'cantidad de pisos', edificio.pisos.length);

  edificio.pisos.forEach(function(piso){

    updateModel(piso, 'cantidad de salas', piso.salas.length);

    piso.salas.forEach(function(sala){

      var tienePasilloVertical = sala.posiciones > 3;

      updateModel(sala, 'posiciones', sala.posiciones);
      updateModel(sala, 'cantidad de filas', sala.filas.length);

      var pasilloVertical = !tienePasilloVertical ? -1 : Math.floor(Math.random() * sala.posiciones) + 1;
      if (sala.pasilloVertical != -1) {
        updateModel(sala, 'pasilloVertical', pasilloVertical);
      }

      sala.filas.forEach(function(fila){
        fila.ocupacion.disponibles = sala.posiciones;
        fila.ocupacion.ocupados = fila.racks.length;
        fila.ocupacion.libres = fila.ocupacion.disponibles - fila.ocupacion.ocupados;

        updateModel(fila, 'disponibles', fila.ocupacion.disponibles);
        updateModel(fila, 'ocupados', fila.ocupacion.ocupados);
        updateModel(fila, 'libres', fila.ocupacion.libres);

        var rackIndex = 1;
        fila.racks.forEach(function(rack){
          if (tienePasilloVertical && rackIndex == pasilloVertical) {
            rackIndex++;
          }
          updateModel(rack, 'posicion', rackIndex);
          rackIndex++;

          var total = (Math.random() >= 0.5) ? 48 : 46;
          rack.ocupacion.disponibles = total;

          var ocupacion = 1;
          var limite = 4;
          rack.equipos.forEach(function(equipo, equipoIndex){
            var promedio = Math.floor(rack.ocupacion.disponibles / rack.equipos.length);
            var posicionInicio = ocupacion;
            var tope = promedio >= limite ? limite : promedio;
            var cuantos = Math.floor(Math.random() * tope) + 1;
            rack.ocupacion.ocupados += cuantos;
            var posicionFin = posicionInicio + cuantos - 1;
            ocupacion += tope;

            updateModel(equipo, 'posicionInicio', posicionInicio);
            updateModel(equipo, 'Posición Inicio', posicionInicio);
            updateModel(equipo, 'posicionFin', posicionFin);
            updateModel(equipo, 'Posición Fin', posicionFin);

            while(!equipo.topologias.length) {
              var random = rows[Math.floor(Math.random() * rows.length)];
              if (equipo.id != random.equipo.id) {

                equipo.topologias.push({
                  edificioId:random.edificio.id,
                  pisoId:random.piso.id,
                  salaId:random.sala.id,
                  filaId:random.fila.id,
                  rackId:random.rack.id,
                  equipoId:random.equipo.id,
                  label:'Edificio: '
                    +random.edificio.nombre+', Piso: '
                    +random.piso.nombre+', Sala: '
                    +random.sala.nombre+', Fila: '
                    +random.fila.nombre+', Rack: '
                    +random.rack.nombre+' Equipo: '
                    +random.equipo.mnemonico,
                });
              }
            }


          });

          rack.ocupacion.libres = rack.ocupacion.disponibles - rack.ocupacion.ocupados;

          updateModel(rack, 'disponibles', rack.ocupacion.disponibles);
          updateModel(rack, 'ocupados', rack.ocupacion.ocupados);
          updateModel(rack, 'libres', rack.ocupacion.libres);

        });

      });

    });

  });

});

